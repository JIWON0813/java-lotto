public class Calculator {
    public int plus(int left, int right) {
        return left + right;
    }

    public int minus(int left, int right) {
        return left - right;
    }

    public int multiply(int left, int right) {
        return left * right;
    }

    public int divide(int left, int right) {
        return left / right;
    }
}
